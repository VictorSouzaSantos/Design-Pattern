
public class Bebida {
	
	public String descricao;
    public double preco;
    public int ml;
    
    
    public String getDescricao() {
        return descricao;
    }

    
    public void setDescricao(String descricao) {
     this.descricao = descricao;
    }

    
    public double getPreco() {
        return preco;
    }

    
    public void setPreco(double preco) {
        this.preco = preco;
    }
    
    public int getMl() {
        return ml;
    }

    public void setMl(int ml){
        this.ml = ml;
    }
}
